// Addition
var  a = 8;
var b = 2;
console.log("Addition:", a + b); // 10

// Subtraction
var  x = 15;
var  y = 5;
console.log("Subtraction:", x - y); // 10

// Multiplication
var  num1 = 4;
var  num2 = 6;
console.log("Multiplication:", num1 * num2); // 24

// Division
var  total = 20;
var  parts = 4;
console.log("Division:", total / parts); // 5

// Modulus (Remainder)
var  m = 17;
var  n = 3;
console.log("Modulus:", m % n); // 2

// Exponentiation
var  base = 3;
